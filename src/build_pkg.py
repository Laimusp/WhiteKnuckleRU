def rm(p):
    if os.path.isdir(p): shutil.rmtree(p, ignore_errors=True)
    elif os.path.exists(p): os.remove(p)

def ensure(p):
    os.makedirs(p, exist_ok=True); return p

def keyidx(ln):
    for i, ch in enumerate(ln):
        if ch == '=' and (i == 0 or ln[i-1] != '\\'): return i
    return -1

# ---------- 1) чистка словаря (минимально и безопасно) ----------
def clean_dict():
    raw = open(GTXT + "/" + DICT, "rb").read()
    if raw[:3] == b"\xef\xbb\xbf": raw = raw[3:]          # снять BOM если вдруг
    txt = raw.decode("utf-8", "replace")
    out = []
    dropped_empty = 0
    for ln in txt.split("\n"):
        ln = ln.rstrip("\r")
        s = ln.strip()
        if s == "" or s == "=":                            # пустые строки и одинокий '='
            dropped_empty += 1; continue
        i = keyidx(ln)
        if i == 0:                                         # ключ пустой -> мусор
            dropped_empty += 1; continue
        out.append(ln)
    body = "\n".join(out) + "\n"
    return body, len(out), dropped_empty

# ---------- 2) переписать конфиг под plugin-relative пути ----------
def rewrite_config():
    cfg = open(GCFG + "/AutoTranslatorConfig.ini", "r", encoding="utf-8-sig").read()
    old = "Translation\\{Lang}"
    new = "plugins\\" + SUB + "\\Translation\\{Lang}"
    n = cfg.count(old)
    cfg = cfg.replace(old, new)
    # контроль
    assert ("Directory=plugins\\%s\\Translation\\{Lang}\\Text" % SUB) in cfg, "Directory не переписан"
    assert "Directory=Translation\\{Lang}" not in cfg, "остался старый Directory"
    assert "OverrideFontTextMeshPro=oswald_ru_sdf" in cfg, "потерян override-шрифт"
    return cfg, n

