# ---------- ICON 256x256 ----------
def make_icon(path):
    try:
        from PIL import Image, ImageDraw, ImageFont
        W = 256
        img = Image.new("RGB", (W, W), (24, 24, 27))
        d = ImageDraw.Draw(img)
        for y in range(W):                                  # вертикальный градиент
            t = y / (W - 1.0)
            c = (int(24 + 10*t), int(24 + 8*t), int(27 + 12*t))
            d.line([(0, y), (W, y)], fill=c)
        d.rectangle([4, 4, W-5, W-5], outline=(70, 70, 76), width=2)
        def font(sz):
            for fn in ("arialbd.ttf", "ariblk.ttf", "segoeuib.ttf"):
                try: return ImageFont.truetype("C:/Windows/Fonts/" + fn, sz)
                except Exception: pass
            return ImageFont.load_default()
        def center(txt, f, cy, fill):
            try:
                b = d.textbbox((0, 0), txt, font=f); w = b[2]-b[0]; h = b[3]-b[1]; oy = b[1]
            except Exception:
                w, h = d.textsize(txt, font=f); oy = 0
            d.text(((W-w)/2.0, cy - h/2.0 - oy), txt, font=f, fill=fill)
        center("WK", font(118), 96, (236, 236, 232))
        center("РУ", font(72), 188, (176, 65, 62))
        img.save(path, "PNG")
        return "PIL"
    except Exception as e:
        # запасной вариант: сплошной валидный PNG 256x256
        W = 256; px = bytes((26, 26, 29))
        row = b"\x00" + px * W
        rawimg = row * W
        comp = zlib.compress(rawimg, 9)
        def chunk(typ, data):
            return struct.pack(">I", len(data)) + typ + data + struct.pack(">I", zlib.crc32(typ + data) & 0xffffffff)
        png = b"\x89PNG\r\n\x1a\n"
        png += chunk(b"IHDR", struct.pack(">IIBBBBB", W, W, 8, 2, 0, 0, 0))
        png += chunk(b"IDAT", comp)
        png += chunk(b"IEND", b"")
        open(path, "wb").write(png)
        return "fallback(%s)" % type(e).__name__

# ---------- zip ----------
def zipdir(src_root, zip_path):
    with zipfile.ZipFile(zip_path, "w", zipfile.ZIP_DEFLATED, compresslevel=9) as z:
        for root, dirs, files in os.walk(src_root):
            for fn in files:
                full = os.path.join(root, fn)
                arc = os.path.relpath(full, src_root).replace("\\", "/")
                z.write(full, arc)
    return os.path.getsize(zip_path)

